// 苗字と名前の間に改行を入れて各行に一文字ずつ表示させるクラス
public class E1_3_Name2 {

	// 苗字と名前の間に改行を入れて一文字ずつ表示させます
	public static void main(String[] args) {
		// 名前の後に改行を入れる出力コードを実行します
		System.out.println("大\n塚\n");
		// 各行に一文字ずつ表示させるよう出力コードを実行します
		System.out.println("朝\n美");

	}

}
